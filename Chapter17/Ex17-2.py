class Kangaroo:
    def __init__(self):
        self.pouch_contents = []
        
    def put_in_pouch(object):
        self.pouch_contents.append(object)

    def __str__(self):
        result = ""
        for item in self.pouch_contents:
            result += item
        return result

t = Kangaroo()
t.put_in_pouch('kanga')
